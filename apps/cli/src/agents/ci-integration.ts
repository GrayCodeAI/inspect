const DEFAULT_TREND_FILE = join(process.cwd(), ".inspect", "trend.json");

export function saveTrendEntry(
  report: TestReport,
  trendFile?: string,
): void {
  const filePath = trendFile ?? DEFAULT_TREND_FILE;
  const dir = dirname(filePath);
  if (!existsSync(dir)) {
    mkdirSync(dir, { recursive: true });
  }

  const a11yScore =
    report.a11y.length > 0
      ? Math.round(
          report.a11y.reduce((s, r) => s + r.score, 0) / report.a11y.length,
        )
      : 0;

  const perfScore =
    report.performance && report.performance.length > 0
      ? Math.round(
          report.performance.reduce((s, r) => s + r.score, 0) /
            report.performance.length,
        )
      : 0;

  const entry: TrendEntry = {
    timestamp: report.timestamp,
    url: report.url,
    scores: {
      overall: report.summary.overallScore,
      a11y: a11yScore,
      security: report.security?.score ?? 0,
      performance: perfScore,
      seo: report.seo?.score ?? 0,
    },
  };

  const line = JSON.stringify(entry) + "\n";
  appendFileSync(filePath, line, "utf-8");
}

export function loadTrend(trendFile?: string): TrendEntry[] {
  const filePath = trendFile ?? DEFAULT_TREND_FILE;

  if (!existsSync(filePath)) {
    return [];
  }

  const content = readFileSync(filePath, "utf-8");
  const lines = content.split("\n").filter((line) => line.trim().length > 0);
  const entries: TrendEntry[] = [];

  for (const line of lines) {
    try {
      const parsed = JSON.parse(line) as TrendEntry;
      entries.push(parsed);
    } catch {
      // Skip malformed lines
    }
  }

  return entries;
}

