// ---------------------------------------------------------------------------
// Types
// ---------------------------------------------------------------------------

export interface QualityGate {
  metric: string;
  threshold: number;
  operator: ">=" | "<=" | ">" | "<" | "==";
}

export interface GateResult {
  gate: QualityGate;
  actual: number;
  passed: boolean;
}

export interface TrendEntry {
  timestamp: string;
  url: string;
  scores: {
    overall: number;
    a11y: number;
    security: number;
    performance: number;
    seo: number;
  };
}

// ---------------------------------------------------------------------------
// GitHub Actions YAML generation
// ---------------------------------------------------------------------------

export function generateGitHubActionsYAML(options?: {
  schedule?: string;
  branches?: string[];
}): string {
  const branches = options?.branches ?? ["main", "develop"];
  const branchList = branches.map(b => `      - ${b}`).join("\n");

  let onBlock = `on:
  push:
    branches:
${branchList}
  pull_request:
    branches:
${branchList}`;

  if (options?.schedule) {
    onBlock += `\n  schedule:
    - cron: '${options.schedule}'`;
  }

  return `# Generated by Inspect — AI-Powered Browser Testing Platform
name: Inspect Tests

${onBlock}

jobs:
  inspect:
    runs-on: ubuntu-latest
    timeout-minutes: 30

    steps:
      - name: Checkout
        uses: actions/checkout@v4

      - name: Setup Node.js
        uses: actions/setup-node@v4
        with:
          node-version: '20'

      - name: Install dependencies
        run: npm ci

      - name: Install Playwright browsers
        run: npx playwright install --with-deps chromium

      - name: Run Inspect tests
        run: npx inspect test --url \${{ vars.INSPECT_URL || 'http://localhost:3000' }} --full --json --output .inspect/reports/ci-report.json
        env:
          ANTHROPIC_API_KEY: \${{ secrets.ANTHROPIC_API_KEY }}
          CI: true

      - name: Upload report
        if: always()
        uses: actions/upload-artifact@v4
        with:
          name: inspect-report
          path: .inspect/reports/
          retention-days: 30

      - name: Comment on PR
        if: github.event_name == 'pull_request' && always()
        uses: actions/github-script@v7
        with:
          script: |
            const fs = require('fs');
            const reportPath = '.inspect/reports/ci-report.json';
            if (fs.existsSync(reportPath)) {
              const report = JSON.parse(fs.readFileSync(reportPath, 'utf8'));
              const score = report.summary.overallScore;
              const passed = report.summary.passed;
              const failed = report.summary.failed;
              const body = \`## Inspect Test Results\\n\\n| Metric | Value |\\n|--------|-------|\\n| Score | \${score}/100 |\\n| Passed | \${passed} |\\n| Failed | \${failed} |\\n\\n[View full report in artifacts]()\`;
              github.rest.issues.createComment({
                issue_number: context.issue.number,
                owner: context.repo.owner,
                repo: context.repo.repo,
                body
              });
            }
`;
}

