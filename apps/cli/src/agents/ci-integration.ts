// Badge SVG generation
// ---------------------------------------------------------------------------

export function generateBadgeSVG(
  label: string,
  score: number,
  thresholds?: { good: number; ok: number },
): string {
  const goodThreshold = thresholds?.good ?? 80;
  const okThreshold = thresholds?.ok ?? 50;

  const color =
    score >= goodThreshold
      ? "#4c1"
      : score >= okThreshold
        ? "#dfb317"
        : "#e05d44";

  const scoreText = `${score}`;
  const labelWidth = label.length * 6.5 + 12;
  const scoreWidth = scoreText.length * 7.5 + 12;
  const totalWidth = labelWidth + scoreWidth;

  return `<svg xmlns="http://www.w3.org/2000/svg" width="${totalWidth}" height="20" role="img" aria-label="${escapeXmlAttr(label)}: ${scoreText}">
  <title>${escapeXmlAttr(label)}: ${scoreText}</title>
  <linearGradient id="s" x2="0" y2="100%">
    <stop offset="0" stop-color="#bbb" stop-opacity=".1"/>
    <stop offset="1" stop-opacity=".1"/>
  </linearGradient>
  <clipPath id="r">
    <rect width="${totalWidth}" height="20" rx="3" fill="#fff"/>
  </clipPath>
  <g clip-path="url(#r)">
    <rect width="${labelWidth}" height="20" fill="#555"/>
    <rect x="${labelWidth}" width="${scoreWidth}" height="20" fill="${color}"/>
    <rect width="${totalWidth}" height="20" fill="url(#s)"/>
  </g>
  <g fill="#fff" text-anchor="middle" font-family="Verdana,Geneva,DejaVu Sans,sans-serif" text-rendering="geometricPrecision" font-size="11">
    <text aria-hidden="true" x="${labelWidth / 2}" y="15" fill="#010101" fill-opacity=".3">${escapeXmlContent(label)}</text>
    <text x="${labelWidth / 2}" y="14">${escapeXmlContent(label)}</text>
    <text aria-hidden="true" x="${labelWidth + scoreWidth / 2}" y="15" fill="#010101" fill-opacity=".3">${scoreText}</text>
    <text x="${labelWidth + scoreWidth / 2}" y="14">${scoreText}</text>
  </g>
</svg>`;
}

