// ---------------------------------------------------------------------------
// Quality gate evaluation
// ---------------------------------------------------------------------------

export function evaluateQualityGates(
  report: TestReport,
  gates: QualityGate[],
): GateResult[] {
  return gates.map((gate) => {
    const actual = resolveMetric(report, gate.metric);
    const passed = compareValue(actual, gate.threshold, gate.operator);
    return { gate, actual, passed };
  });
}

function resolveMetric(report: TestReport, metric: string): number {
  switch (metric) {
    case "overall":
      return report.summary.overallScore;
    case "a11y": {
      if (report.a11y.length === 0) return 0;
      return Math.round(
        report.a11y.reduce((sum, r) => sum + r.score, 0) / report.a11y.length,
      );
    }
    case "security":
      return report.security?.score ?? 0;
    case "performance": {
      const perf = report.performance;
      if (!perf || perf.length === 0) return 0;
      return Math.round(
        perf.reduce((sum, r) => sum + r.score, 0) / perf.length,
      );
    }
    case "seo":
      return report.seo?.score ?? 0;
    case "responsive":
      return report.responsive?.score ?? 0;
    case "pass_rate": {
      if (report.summary.total === 0) return 0;
      return Math.round(
        (report.summary.passed / report.summary.total) * 100,
      );
    }
    case "failures":
      return report.summary.failed;
    case "duration":
      return report.summary.duration;
    default:
      return 0;
  }
}

function compareValue(
  actual: number,
  threshold: number,
  operator: QualityGate["operator"],
): boolean {
  switch (operator) {
    case ">=":
      return actual >= threshold;
    case "<=":
      return actual <= threshold;
    case ">":
      return actual > threshold;
    case "<":
      return actual < threshold;
    case "==":
      return actual === threshold;
    default:
      return false;
  }
}

// ---------------------------------------------------------------------------
// PR comment generation
// ---------------------------------------------------------------------------

export function generatePRComment(
  report: TestReport,
  previousReport?: TestReport,
): string {
  const score = report.summary.overallScore;
  const statusEmoji = score >= 80 ? "pass" : score >= 50 ? "warn" : "fail";
  const statusLabel =
    statusEmoji === "pass"
      ? "PASSED"
      : statusEmoji === "warn"
        ? "WARNING"
        : "FAILED";

  // Compute category scores
  const a11yScore =
    report.a11y.length > 0
      ? Math.round(
          report.a11y.reduce((s, r) => s + r.score, 0) / report.a11y.length,
        )
      : null;
  const securityScore = report.security?.score ?? null;
  const perfScore =
    report.performance && report.performance.length > 0
      ? Math.round(
          report.performance.reduce((s, r) => s + r.score, 0) /
            report.performance.length,
        )
      : null;
  const seoScore = report.seo?.score ?? null;
  const responsiveScore = report.responsive?.score ?? null;

  // Build score table
  let scoreTable = `| Category | Score |`;
  if (previousReport) {
    scoreTable += ` Delta |`;
  }
  scoreTable += `\n|----------|-------|`;
  if (previousReport) {
    scoreTable += `-------|`;
  }
  scoreTable += "\n";

  const addRow = (label: string, current: number | null, prevReport?: TestReport, metricKey?: string): string => {
    const val = current !== null ? `${current}/100` : "N/A";
    if (previousReport && prevReport && metricKey) {
      const prev = resolveMetric(prevReport, metricKey);
      if (current !== null) {
        const delta = current - prev;
        const deltaStr = delta > 0 ? `+${delta}` : delta < 0 ? `${delta}` : `0`;
        return `| ${label} | ${val} | ${deltaStr} |\n`;
      }
      return `| ${label} | ${val} | - |\n`;
    }
    if (previousReport) {
      return `| ${label} | ${val} | - |\n`;
    }
    return `| ${label} | ${val} |\n`;
  };

  scoreTable += addRow("Overall", score, previousReport, "overall");
  scoreTable += addRow("Accessibility", a11yScore, previousReport, "a11y");
  scoreTable += addRow("Security", securityScore, previousReport, "security");
  scoreTable += addRow("Performance", perfScore, previousReport, "performance");
  scoreTable += addRow("SEO", seoScore, previousReport, "seo");
  scoreTable += addRow("Responsive", responsiveScore, previousReport, "responsive");

  // Summary
  const summary = `**${report.summary.passed}** passed, **${report.summary.failed}** failed, **${report.summary.skipped}** skipped out of **${report.summary.total}** steps (${(report.summary.duration / 1000).toFixed(1)}s)`;

  // Top 5 issues
  const issues: Array<{ severity: string; description: string }> = [];

  for (const a11yReport of report.a11y) {
    for (const issue of a11yReport.issues) {
      issues.push({ severity: issue.severity, description: `[A11y] ${issue.rule}: ${issue.description}` });
    }
  }
  if (report.security) {
    for (const issue of report.security.issues) {
      issues.push({ severity: issue.severity, description: `[Security] ${issue.title}: ${issue.description}` });
    }
  }
  if (report.seo) {
    for (const issue of report.seo.issues) {
      issues.push({ severity: issue.severity, description: `[SEO] ${issue.rule}: ${issue.description}` });
    }
  }

  // Sort by severity
  const severityOrder: Record<string, number> = { critical: 0, serious: 1, high: 1, moderate: 2, medium: 2, minor: 3, low: 4, info: 5 };
  issues.sort((a, b) => (severityOrder[a.severity] ?? 99) - (severityOrder[b.severity] ?? 99));

  let issuesList = "";
  if (issues.length > 0) {
    const topIssues = issues.slice(0, 5);
    issuesList = `\n### Top Issues\n\n`;
    for (const issue of topIssues) {
      issuesList += `- **${issue.severity}**: ${issue.description}\n`;
    }
    if (issues.length > 5) {
      issuesList += `\n_...and ${issues.length - 5} more issues_\n`;
    }
  }

  return `## Inspect Test Results — ${statusLabel}

**Score: ${score}/100** | ${report.url}

### Summary

${summary}

### Scores

${scoreTable}${issuesList}

---
_Generated by [Inspect](https://github.com/nicholasgriffintn/inspect) — AI-Powered Browser Testing Platform_
`;
}

// ---------------------------------------------------------------------------
// Slack payload generation
// ---------------------------------------------------------------------------

export function generateSlackPayload(
  report: TestReport,
  webhookUrl?: string,
): Record<string, unknown> {
  const score = report.summary.overallScore;
  const color = score >= 80 ? "#22c55e" : score >= 50 ? "#eab308" : "#ef4444";
  const statusText = score >= 80 ? "Passed" : score >= 50 ? "Warning" : "Failed";

  const a11yScore =
    report.a11y.length > 0
      ? Math.round(
          report.a11y.reduce((s, r) => s + r.score, 0) / report.a11y.length,
        )
      : null;

  const fields: Array<Record<string, unknown>> = [
    {
      type: "mrkdwn",
      text: `*Overall Score*\n${score}/100`,
    },
    {
      type: "mrkdwn",
      text: `*Tests*\n${report.summary.passed}/${report.summary.total} passed`,
    },
  ];

  if (a11yScore !== null) {
    fields.push({
      type: "mrkdwn",
      text: `*Accessibility*\n${a11yScore}/100`,
    });
  }
  if (report.security) {
    fields.push({
      type: "mrkdwn",
      text: `*Security*\n${report.security.score}/100`,
    });
  }
  if (report.performance && report.performance.length > 0) {
    const avgPerf = Math.round(
      report.performance.reduce((s, r) => s + r.score, 0) /
        report.performance.length,
    );
    fields.push({
      type: "mrkdwn",
      text: `*Performance*\n${avgPerf}/100`,
    });
  }
  if (report.seo) {
    fields.push({
      type: "mrkdwn",
      text: `*SEO*\n${report.seo.score}/100`,
    });
  }

  const blocks: Array<Record<string, unknown>> = [
    {
      type: "header",
      text: {
        type: "plain_text",
        text: `Inspect: ${statusText} — ${score}/100`,
        emoji: true,
      },
    },
    {
      type: "section",
      text: {
        type: "mrkdwn",
        text: `*<${report.url}|${report.title}>*\n${report.summary.passed} passed, ${report.summary.failed} failed, ${report.summary.skipped} skipped | ${(report.summary.duration / 1000).toFixed(1)}s`,
      },
    },
    {
      type: "section",
      fields,
    },
  ];

  // Add failed steps if any
  if (report.summary.failed > 0) {
    const failedSteps = report.results
      .filter((s) => s.status === "fail")
      .slice(0, 5);
    const failedText = failedSteps
      .map((s) => `- ${s.description}${s.error ? `: ${s.error}` : ""}`)
      .join("\n");
    blocks.push({
      type: "section",
      text: {
        type: "mrkdwn",
        text: `*Failed Steps:*\n${failedText}`,
      },
    });
  }

  blocks.push({
    type: "context",
    elements: [
      {
        type: "mrkdwn",
        text: `Inspect — AI-Powered Browser Testing | ${report.timestamp}`,
      },
    ],
  });

  const payload: Record<string, unknown> = {
    blocks,
    attachments: [
      {
        color,
        blocks: [],
      },
    ],
  };

  if (webhookUrl) {
    payload.webhookUrl = webhookUrl;
  }

  return payload;
}

